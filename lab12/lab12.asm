; This program takes 3 values from x5000, x5001, and x5002 for background,
; foreground, and letter respectively. Background bits are identified by 0s and
; foreground bits are identified as 1s. The program does bit shift left and
; checks for MSB == 1, if it's 1, then output foreground char, otherwise output
; background char. By default OUTPUT 0, only OUTPUT 1 when foreground bit. 
;
; TABLE OF REGISTER INFORMATION AND USAGE
; R0 -> Used for storing the OUTPUT character only
; R1 -> Holds addresses. First iterates through x5000 to x5002. Second iterates
;           through FONT_DATA for respective letter
; R2 -> Holds values stored at the address being held in R1. Is later bit
;           shifted for checking each bit in a line
; R3 -> Counter register for column. 8 columns
; R4 -> Counter register for rows. 16 rows
;
; LABELS USED
; N_COL -> ABRV. NEXT_COLUMN        Loop for column iterator
; N_ROW -> ABRV. NEXT_ROW           Loop for row iterator
; L_LOC -> ABRV. LETTER_LOCATION    Loop for finding the location of the font
;                                       in FONT_DATA
; LN    -> Taken from println       Stores newline char AKA single line feed
; ZEROS ->                          Stores character for BG or 0 bits
; ONES  ->                          Stores character for FG or 1 bits
; D_LOC -> ABRV. DATA_LOCATION      Stores the location of data (x5000)

    .ORIG   x3000
    AND     R3,R3,#0                ; Init R3 to 0
    ADD     R3,R3,#8                ; Set R3 to 8
    AND     R4,R4,#0                ; Init R4 to 0
    ADD     R4,R4,#8                ; Set R4 to 8
    ADD     R4,R4,#8                ; Set R4 to 16
    LD      R1,D_LOC                ; Set R1 to value of D_LOC
    LDR     R2,R1,#0                ; Set R2 to the ZEROS value (x5000)
    ST      R2,ZEROS                ; Set mem at loc ZEROS to value of R2
    ADD     R1,R1,#1                ; Move R1 Location forward
    LDR     R2,R1,#0                ; Set R2 to the ONES value  (x5001)
    ST      R2,ONES                 ; Set mem at loc ONES to value of R2
    ADD     R1,R1,#1                ; Move R1 Location forward
    LDR     R2,R1,#0                ; Set R2 to the LETTER value(x5002)
    LEA     R1,FONT_DATA            ; Set R1 to location of FONT_DATA
    ADD     R2,R2,#0                ; Set CC of R2
    BRp     L_LOC

N_COL                               ; Iterative Construct NEXT_COLUMN
    LD      R0,ZEROS                ; Set R0 as ZEROS by default
    ADD     R2,R2,#0                ; Set CC of R2
    BRzp    #1                      ; Skip next step if 0 or positive
    LD      R0,ONES                 ; Set R0 as ONES
    OUT                             ; OUTPUT THE CHAR
    ADD     R2,R2,R2                ; BITSHIFT R2 LEFT
    ADD     R3,R3,#-1               ; R3 <- R3 - 1
    BRp     N_COL                   ; LOOP until R3 is 0

N_ROW                               ; Iterative construct NEXT_ROW
    LD      R0,LN                   ; Set R0 to Line End char
    OUT                             ; OUTPUT NEWLINE CHAR
    AND     R3,R3,#0                ; Set R3 to 0 in case
    ADD     R3,R3,#8                ; Set R3 to 8
    ADD     R1,R1,#1                ; Move forward in R1 loc (GO down 1 row)
    LDR     R2,R1,#0                ; Set R2 to value stored at the location stored in R1
    ADD     R4,R4,#-1               ; R4 <- R4 - 1
    BRp     N_COL                   ; JUMP TO N_COL <- LOOPS UNTIL R4 is 0
    HALT                            ; TRAP x25 AKA HALT

L_LOC
    ADD     R1,R1,#8                ; Add 16
    ADD     R1,R1,#8                ; Add 16
    ADD     R2,R2,#-1               ; R2 <- R2 - 1
    BRp     L_LOC                   ; Loop until R2 is 0
    LDR     R2,R1,#0                ; Set R2 as value stored in location stored by R1
    BR      N_COL

LN
    .FILL   x000A                   ; NEWLINE CHAR

ZEROS
    .FILL   x0000                   ; ZEROS filler

ONES
    .FILL   x0000                   ; ONES filler

D_LOC
    .FILL   x5000                   ; DATA is stored in x5000 - x5002


